import numpy as np
from model import TextEmbedder

def run_tests():
    print("Запуск тестов модели...\n")
    embedder = TextEmbedder()
    
    # ТЕСТ 1: Проверка размерности
    vec = embedder.get_embedding("Тестовая фраза", "query")
    print(f"[ТЕСТ 1] Размерность вектора: {len(vec)}")
    if len(vec) == 384:
        print("✅ Успешно: Размерность совпадает (384).")
    else:
        print("❌ Ошибка: Ожидали 384, получили другое число.")

    # ТЕСТ 2: Проверка нормализации (длина вектора должна быть 1)
    norm = np.linalg.norm(vec)
    print(f"\n[ТЕСТ 2] Длина (норма) вектора: {norm:.4f}")
    if np.isclose(norm, 1.0):
        print("✅ Успешно: Вектор нормализован (длина = 1).")
    else:
        print("❌ Ошибка: Вектор не нормализован!")

    # ТЕСТ 3: Семантическая близость (Самое важное!)
    print("\n[ТЕСТ 3] Проверка логики смыслов:")
    
    # Вопрос про отпуск
    q = embedder.get_embedding("как мне уйти в отпуск?", "query")
    
    # Вариант А: Похожий по смыслу (про отдых)
    a = embedder.get_embedding("инструкция по оформлению ежегодного отдыха", "passage")
    
    # Вариант Б: Вообще про другое (про технику)
    b = embedder.get_embedding("как настроить роутер через консоль", "passage")
    
    # Считаем скалярное произведение (наш "счетчик похожести")
    sim_a = np.dot(q, a)
    sim_b = np.dot(q, b)
    
    print(f"-> Сходство 'отпуск' + 'отдых': {sim_a:.4f}")
    print(f"-> Сходство 'отпуск' + 'роутер': {sim_b:.4f}")
    
    if sim_a > sim_b:
        print("✅ Успешно: Модель понимает, что отпуск ближе к отдыху, чем к роутеру.")
    else:
        print("❌ Ошибка: Модель не видит разницы в смысле!")

    # ТЕСТ 4: Сплиттер (нарезка текста)
    print("\n[ТЕСТ 4] Проверка сплиттера:")
    long_text = "А" * 1200 # Очень длинная строка
    chunks = embedder.split_text(long_text, chunk_size=500, overlap=100)
    print(f"-> Создано чанков: {len(chunks)}")
    if len(chunks) > 1:
        print("✅ Успешно: Текст успешно нарезан на куски.")
    else:
        print("❌ Ошибка: Сплиттер не сработал.")

if __name__ == "__main__":
    run_tests()